# ✅ Задание 2: Счётчик попыток
# Сделай программу с циклом while, где пользователь должен ввести "да", чтобы выйти.
# Пока он вводит что угодно другое — продолжается:
#   Вы готовы выйти? → нет
#   Повторите ещё раз
# Когда введёт "да" — вывести "Программа завершена".

while True:
    x = input("Вы готовы выйти? Да/Нет: ").lower()
    if x != "да":
        print("Повторите еще раз")
        continue
    break
print("Программа завершена")


