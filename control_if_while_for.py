# ✅ Задание 4: Угадай число (1–20)
# Программа случайно загадывает число от 1 до 20.
# Пользователь пытается угадать.
# После каждого ввода:
#   "Больше" / "Меньше"
# После угадывания: "Угадал за X попыток!"

import random

a = random.randint(1, 20)
x = int(input("Введите ваше число от 1 до 20, человек: "))
b = 1

while not (1 <= x <= 20):
    x = int(input("Не пытайся обмануть систему, человек. Введи число от 1 до 20: "))
    continue

while a != x:
    b += 1
    if a > x:
        print("Больше")
        x = int(input("Попробуйте еще раз: "))
    if a < x:
        print("Меньше")
        x = int(input("Попробуйте еще раз: "))
    continue

print(f"Угадал за {b} попыток!")


